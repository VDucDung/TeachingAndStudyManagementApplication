    private void showGroupInfo() {
        try {
            ArrayList<SubjectClass> clList = (ArrayList<SubjectClass>) DBEngine.readObjectFromFile("C:\\Database\\CLASSES.txt");
            for (SubjectClass cl : clList) {
                if (cl.getClName().equals(clName)) {
                    for (Group gr : cl.getGrList()) {
                        if (gr.getGrID().equals(grID)) {
                            loadTable(gr.getMemberList());
                        }
                    }
                }
            }
        } catch (Exception ex) {
            //System.out.println(ex.toString());
            JOptionPane.showMessageDialog(this, "Lỗi kết nỗi cơ sở dữ liệu!", "Lỗi!", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void loadTable(ArrayList<Student> memberList) {
        tblMemberList.setModel(new MemberListTableModel(memberList));
    }

