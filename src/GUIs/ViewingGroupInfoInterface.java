    private void btnAddEvaluationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEvaluationActionPerformed
        // TODO add your handling code here:
        if (txtGroupEvaluation.getText().trim().equals("")) {
            JOptionPane.showMessageDialog(this, "Bạn chưa nhập đánh giá!", "Cảnh báo!", JOptionPane.INFORMATION_MESSAGE);
            txtGroupEvaluation.requestFocus();
        } else {
            try {
                ArrayList<SubjectClass> clList = (ArrayList<SubjectClass>) DBEngine.readObjectFromFile("C:\\Database\\CLASSES.txt");
                for (SubjectClass cl : clList) {
                    if (clName.equals(cl.getClName())) {
                        for (Group gr : cl.getGrList()) {
                            if (gr.getGrID().equals(grID)) {
                                gr.setGrEvaluation(txtGroupEvaluation.getText());
                            }
                        }
                    }
                }
                DBEngine.writeObjectToFile("C:\\Database\\CLASSES.txt", clList);
                JOptionPane.showMessageDialog(this, "Thêm đánh giá thành công!", "Thông báo!", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, "Không thể thêm đánh giá do lỗi kết nỗi cơ sở dữ liệu!", "Lỗi!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnAddEvaluationActionPerformed

